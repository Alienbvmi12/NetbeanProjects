    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         try {
            
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            Statement stm = con.createStatement();
            rs = stm.executeQuery("select * from barang where id_barang='"+tabel_klik+"'");
            if(rs.next()){
                String spek = rs.getString("id_barang");
                id.setText(spek);
                String nama = rs.getString("deskripsi");
                spec.setText(nama);
                String kt = rs.getString("nama_barang");
                kateg.setText(kt);
                 String mrk = rs.getString("merk");
                merk.setText(mrk);
                 String st = rs.getString("stok");
                stok.setText(st);
            }
        } catch (Exception e) {} 
    }//GEN-LAST:event_jTable1MouseClicked

