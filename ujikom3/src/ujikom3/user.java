   public void TampilData() {
        tm = new DefaultTableModel();
        tm.addColumn("ID Spesifikasi");
        tm.addColumn("Spesifikasi");
        tm.addColumn("Kategori");
        tm.addColumn("Merk");
        tm.addColumn("Stok");
        jTable1.setModel(tm);
        
        try {
            
            sql = "select * from barang, kategori where nama_barang=id_kateg";
            java.sql.ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tm.addRow(new Object[]{
                    res.getInt("id_barang"),
                    res.getString("deskripsi"),
                    res.getString("nm_kateg"),
                    res.getString("merk"),
                    res.getInt("stok")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

   

