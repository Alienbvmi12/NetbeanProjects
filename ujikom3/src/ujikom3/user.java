    private void tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tambahMouseClicked
        try {

            PreparedStatement stmt = con.prepareStatement("insert into barang(id_barang, deskripsi, nama_barang, merk, stok) values(?,?,?,?,?)");
            stmt.setString(1, id.getText());
            stmt.setString(2, spec.getText());
            stmt.setString(3, kateg.getText());
            stmt.setString(4, merk.getText());
            stmt.setString(5, stok.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            hapuslayar();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tambahMouseClicked

